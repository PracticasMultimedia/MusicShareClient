    public void printEcualizer(float[] bandas) {

        turno++;

        for (int i = 0; i < bandas.length; i++) {

            if (bandas[i] > max) {
                max = bandas[i];
            }

            if (bandas[i] < min) {
                min = bandas[i];
            }
        }

        for (int i = 0; i < bandas.length; i++) {
            bandas[i] = (float) ((bandas[i] - min) * 100) / ((max / 4) - min);
        }

        jProgressBar1.setValue((int) bandas[0]);
        jProgressBar2.setValue((int) bandas[1]);
        jProgressBar3.setValue((int) bandas[2]);
        jProgressBar4.setValue((int) bandas[3]);
        jProgressBar5.setValue((int) bandas[4]);
        jProgressBar6.setValue((int) bandas[5]);
        jProgressBar7.setValue((int) bandas[6]);
    }

